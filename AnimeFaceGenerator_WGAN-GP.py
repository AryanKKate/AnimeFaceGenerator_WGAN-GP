def get_dataloader(root: str = "data/", subset_size: int = SUBSET_SIZE) -> DataLoader:
    """
    Build a DataLoader over a random subset of the anime-face dataset.

    Each image is:
      1. Resized to IMAGE_SIZE × IMAGE_SIZE
      2. Centre-cropped to IMAGE_SIZE × IMAGE_SIZE
      3. Converted to a tensor in [0, 1]
      4. Normalised to [-1, 1] (matching the Generator's Tanh output)
    """
    transform = transforms.Compose([
        transforms.Resize(IMAGE_SIZE),
        transforms.CenterCrop(IMAGE_SIZE),
        transforms.ToTensor(),
        transforms.Normalize([0.5] * 3, [0.5] * 3),   # → [-1, 1]
    ])

    full_dataset = ImageFolder(root=root, transform=transform)
    indices      = np.random.choice(len(full_dataset), subset_size, replace=False)
    dataset      = Subset(full_dataset, indices)

    print(f"[Dataset] Using {len(dataset)} / {len(full_dataset)} images.")

    return DataLoader(
        dataset,
        batch_size=BATCH_SIZE,
        shuffle=True,
        num_workers=2,
        pin_memory=(DEVICE == "cuda"),
    )



def get_inception_features(images: torch.Tensor, inception: nn.Module) -> np.ndarray:

    resize = transforms.Resize((299, 299), antialias=True)
    imgs   = resize((images + 1.0) / 2.0).clamp(0, 1)   # [-1,1] → [0,1]

    with torch.no_grad():
        features = inception(imgs)                       # (N, 2048)
    return features.cpu().numpy()


def inception_score(
    generator: nn.Module,
    n_samples: int = 5000,
    batch_size: int = 64,
    splits: int = 10,
    device: str = DEVICE,
) -> tuple[float, float]:

    print("[Eval] Computing Inception Score …")

    # InceptionV3 with softmax output
    inception = inception_v3(pretrained=True, transform_input=False).to(device).eval()
    inception.fc = nn.Identity()   # we want the final 1000-class logits, so restore
    inception    = inception_v3(pretrained=True, transform_input=False).to(device).eval()

    resize = transforms.Resize((299, 299), antialias=True)
    all_probs = []

    with torch.no_grad():
        for _ in range(math.ceil(n_samples / batch_size)):
            noise  = torch.randn(batch_size, Z_DIM, 1, 1, device=device)
            fake   = generator(noise)
            imgs   = resize((fake + 1.0) / 2.0).clamp(0, 1)
            logits = inception(imgs)
            probs  = torch.softmax(logits, dim=1).cpu().numpy()
            all_probs.append(probs)

    all_probs = np.concatenate(all_probs, axis=0)[:n_samples]  # (N, 1000)
    chunk_scores = []

    for k in range(splits):
        chunk = all_probs[k * (n_samples // splits) : (k + 1) * (n_samples // splits)]
        py    = chunk.mean(axis=0, keepdims=True)                      # marginal p(y)
        kl    = chunk * (np.log(chunk + 1e-10) - np.log(py + 1e-10))  # KL per image
        chunk_scores.append(np.exp(kl.sum(axis=1).mean()))

    mean_is, std_is = float(np.mean(chunk_scores)), float(np.std(chunk_scores))
    print(f"  IS = {mean_is:.3f} ± {std_is:.3f}")
    return mean_is, std_is


def compute_fid(
    generator: nn.Module,
    real_loader: DataLoader,
    n_fake: int = 5000,
    device: str = DEVICE,
) -> float:

    print("[Eval] Computing FID …")

    # InceptionV3 truncated at pool3 (2048-d features)
    inception = inception_v3(pretrained=True, transform_input=False).to(device).eval()
    inception.fc       = nn.Identity()
    inception.dropout  = nn.Identity()

    resize = transforms.Resize((299, 299), antialias=True)

    def extract_features(images: torch.Tensor) -> np.ndarray:
        imgs = resize((images + 1.0) / 2.0).clamp(0, 1).to(device)
        with torch.no_grad():
            feats = inception(imgs)
        return feats.cpu().numpy()

    # Real features
    real_feats = []
    for imgs, _ in real_loader:
        real_feats.append(extract_features(imgs))
        if len(np.concatenate(real_feats)) >= n_fake:
            break
    real_feats = np.concatenate(real_feats)[:n_fake]

    # Fake features
    fake_feats = []
    collected  = 0
    while collected < n_fake:
        batch  = min(64, n_fake - collected)
        noise  = torch.randn(batch, Z_DIM, 1, 1, device=device)
        fake   = generator(noise)
        fake_feats.append(extract_features(fake))
        collected += batch
    fake_feats = np.concatenate(fake_feats)[:n_fake]

    # Statistics
    mu_r, sigma_r = real_feats.mean(0), np.cov(real_feats, rowvar=False)
    mu_f, sigma_f = fake_feats.mean(0), np.cov(fake_feats, rowvar=False)

    # FID formula
    diff     = mu_r - mu_f
    cov_mean, _ = linalg.sqrtm(sigma_r @ sigma_f, disp=False)
    if np.iscomplexobj(cov_mean):
        cov_mean = cov_mean.real

    fid = float(diff @ diff + np.trace(sigma_r + sigma_f - 2.0 * cov_mean))
    print(f"  FID = {fid:.2f}")
    return fid



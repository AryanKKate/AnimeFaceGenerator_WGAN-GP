def main():


    loader = get_dataloader(root="data/", subset_size=SUBSET_SIZE)

    G = Generator(Z_DIM).to(DEVICE)
    C = Critic().to(DEVICE)

    G.apply(weights_init)
    C.apply(weights_init)

    print(f"[Model] Generator params:  {sum(p.numel() for p in G.parameters()):,}")
    print(f"[Model] Critic params:     {sum(p.numel() for p in C.parameters()):,}")

    opt_G = optim.Adam(G.parameters(), lr=LR, betas=(0.0, 0.9))
    opt_C = optim.Adam(C.parameters(), lr=LR, betas=(0.0, 0.9))

    # Fixed noise for consistent progress tracking across epochs
    fixed_noise = torch.randn(32, Z_DIM, 1, 1, device=DEVICE)

    history = train(loader, G, C, opt_G, opt_C, fixed_noise, epochs=EPOCHS, device=DEVICE)

    torch.save(G.state_dict(), "generator.pth")
    torch.save(C.state_dict(), "critic.pth")
    print("[Checkpoint] Models saved → generator.pth, critic.pth")

    plot_loss_curves(history)

    G.eval()
    mean_is, std_is = inception_score(G, n_samples=5000, device=DEVICE)
    fid = compute_fid(G, loader, n_fake=5000, device=DEVICE)

    print("\n╔══════════════════════════════╗")
    print("║      Evaluation Results      ║")
    print("╠══════════════════════════════╣")
    print(f"║  Inception Score: {mean_is:.3f} ± {std_is:.3f}")
    print(f"║  FID Score:       {fid:.2f}")
    print("╚══════════════════════════════╝")

    # Save metrics to text file
    with open("outputs/metrics.txt", "w") as f:
        f.write(f"Inception Score: {mean_is:.4f} ± {std_is:.4f}\n")
        f.write(f"FID Score:       {fid:.4f}\n")
    print("[Eval] Metrics saved → outputs/metrics.txt")


if __name__ == "__main__":
    main()


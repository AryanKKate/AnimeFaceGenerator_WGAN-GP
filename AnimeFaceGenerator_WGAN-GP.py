def plot_loss_curves(history: dict, save_path: str = "outputs/loss_curves.png") -> None:
    """Plot critic and generator loss curves and save to disk."""
    try:
        import matplotlib.pyplot as plt

        epochs = range(1, len(history["loss_C"]) + 1)
        fig, axes = plt.subplots(1, 2, figsize=(12, 4))

        axes[0].plot(epochs, history["loss_C"], label="Critic Loss", color="steelblue")
        axes[0].set_title("Critic Loss per Epoch")
        axes[0].set_xlabel("Epoch"); axes[0].set_ylabel("Loss")
        axes[0].legend(); axes[0].grid(True, alpha=0.3)

        axes[1].plot(epochs, history["loss_G"], label="Generator Loss", color="tomato")
        axes[1].set_title("Generator Loss per Epoch")
        axes[1].set_xlabel("Epoch"); axes[1].set_ylabel("Loss")
        axes[1].legend(); axes[1].grid(True, alpha=0.3)

        plt.tight_layout()
        plt.savefig(save_path, dpi=150)
        plt.close()
        print(f"[Viz] Loss curves saved → {save_path}")
    except ImportError:
        print("[Viz] matplotlib not available; skipping loss plot.")



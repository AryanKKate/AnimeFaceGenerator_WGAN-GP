# 4. WGAN-GP Training Utilities
# ─────────────────────────────────────────────

def gradient_penalty(
    critic: nn.Module,
    real: torch.Tensor,
    fake: torch.Tensor,
    device: str = DEVICE,
) -> torch.Tensor:

    batch_size, C, H, W = real.shape
    eps           = torch.rand(batch_size, 1, 1, 1, device=device).expand_as(real)
    interpolated  = (eps * real + (1.0 - eps) * fake).requires_grad_(True)

    critic_out = critic(interpolated)

    grads = torch.autograd.grad(
        outputs=critic_out,
        inputs=interpolated,
        grad_outputs=torch.ones_like(critic_out),
        create_graph=True,
        retain_graph=True,
    )[0]                                         # shape: (N, C, H, W)

    grad_norm = grads.view(batch_size, -1).norm(2, dim=1)   # L2 norm per sample
    return ((grad_norm - 1.0) ** 2).mean()


def weights_init(module: nn.Module) -> None:

    classname = type(module).__name__
    if "Conv" in classname:
        nn.init.normal_(module.weight.data, 0.0, 0.02)
    elif "BatchNorm" in classname:
        nn.init.normal_(module.weight.data, 1.0, 0.02)
        nn.init.constant_(module.bias.data, 0.0)


# ─────────────────────────────────────────────
# 5. Training Loop
# ─────────────────────────────────────────────

def train(
    loader: DataLoader,
    generator: nn.Module,
    critic: nn.Module,
    opt_G: optim.Optimizer,
    opt_C: optim.Optimizer,
    fixed_noise: torch.Tensor,
    epochs: int = EPOCHS,
    device: str = DEVICE,
) -> dict:

    os.makedirs("outputs", exist_ok=True)
    history = {"loss_C": [], "loss_G": []}

    for epoch in range(epochs):
        epoch_loss_C, epoch_loss_G, n_batches = 0.0, 0.0, 0

        loop = tqdm(loader, desc=f"Epoch [{epoch+1}/{epochs}]", leave=True)

        for real, _ in loop:
            real          = real.to(device)
            cur_batch     = real.shape[0]

            # Critic update (CRITIC_ITERATIONS times)
            for _ in range(CRITIC_ITERATIONS):
                noise        = torch.randn(cur_batch, Z_DIM, 1, 1, device=device)
                fake         = generator(noise).detach()   # no generator grad needed

                loss_C = (
                    -(critic(real).mean() - critic(fake).mean())  # Wasserstein distance (negated)
                    + LAMBDA_GP * gradient_penalty(critic, real, fake, device)
                )

                opt_C.zero_grad()
                loss_C.backward()
                opt_C.step()

            # ── Generator update
            noise   = torch.randn(cur_batch, Z_DIM, 1, 1, device=device)
            fake    = generator(noise)
            loss_G  = -critic(fake).mean()

            opt_G.zero_grad()
            loss_G.backward()
            opt_G.step()

            epoch_loss_C += loss_C.item()
            epoch_loss_G += loss_G.item()
            n_batches    += 1

            loop.set_postfix(loss_C=f"{loss_C.item():.3f}", loss_G=f"{loss_G.item():.3f}")

        # Record epoch averages
        avg_C = epoch_loss_C / n_batches
        avg_G = epoch_loss_G / n_batches
        history["loss_C"].append(avg_C)
        history["loss_G"].append(avg_G)
        print(f"  → Avg loss_C: {avg_C:.4f} | Avg loss_G: {avg_G:.4f}")

        # Save sample grid
        with torch.no_grad():
            fake_samples = generator(fixed_noise)
        save_image(fake_samples, f"outputs/epoch_{epoch+1:02d}.png", normalize=True, nrow=8)

    return history


